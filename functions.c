#include "functions.h"
	
	
	float addition (float number1, float number2)
	{	
		return  number1 + number2;
	}


	float subtraction (float number1, float number2)
	{
		return number1 - number2;
	}

	float division (float number1, float number2)
	{
		return number1 / number2;
	}

	float multiplication (float number1, float number2)
	{
		return number1 * number2;
	}


